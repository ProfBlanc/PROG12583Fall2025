"""
calculation are done between two number values
    +   addition
    -   substraction
    *   multiplication
    /   division    => float values
    //  integer division    => int value by truncating (removing decimals)
    **  exponontial     

"""

n1 = 10
n2 = 3

print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2)
print(n1 ** n2)

n3 = round(1.235, 2)
print(n3)
